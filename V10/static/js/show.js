function show_replies() {
    var x = document.getElementById("replies");
    if (x.style.display==='none') {
        x.style.display = 'block';
    }
    else {
        x.style.display = 'none';
    }
}

function show_input() {
    var x = document.getElementById("input");
    if (x.style.display==='none') {
        x.style.display = 'block';
    }
    else {
        x.style.display = 'none';
    }
}